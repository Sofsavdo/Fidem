export default dict;

export function detectLang() {
  const saved = localStorage.getItem("fidem_lang");
  if (saved && dict[saved]) return saved;
  try {
    const tg = window.Telegram?.WebApp;
    const lc = tg?.initDataUnsafe?.user?.language_code;
    if (lc && dict[lc]) return lc;
    if (lc === "kk") return "ru";
  } catch {}
  const navLang = (navigator.language || "uz").slice(0, 2);
  if (dict[navLang]) return navLang;
  return "uz";
}
